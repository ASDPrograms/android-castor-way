package com.example.castorway.modelsDB;

public class Premios {
    private int idPremio;
    private int idCastor;
    private String nombrePremio;
    private String nivelPremio;
    private String categoriaPremio;
    private String tipoPremio;
    private int costoPremio;
    private String infoExtraPremio;
    private int estadoPremio;
    private int favorito;
    private String rutaImagenHabito;

    public int getIdPremio() {
        return idPremio;
    }

    public void setIdPremio(int idPremio) {
        this.idPremio = idPremio;
    }

    public int getIdCastor() {
        return idCastor;
    }

    public void setIdCastor(int idCastor) {
        this.idCastor = idCastor;
    }

    public String getNombrePremio() {
        return nombrePremio;
    }

    public void setNombrePremio(String nombrePremio) {
        this.nombrePremio = nombrePremio;
    }

    public String getNivelPremio() {
        return nivelPremio;
    }

    public void setNivelPremio(String nivelPremio) {
        this.nivelPremio = nivelPremio;
    }

    public String getCategoriaPremio() {
        return categoriaPremio;
    }

    public void setCategoriaPremio(String categoriaPremio) {
        this.categoriaPremio = categoriaPremio;
    }

    public String getTipoPremio() {
        return tipoPremio;
    }

    public void setTipoPremio(String tipoPremio) {
        this.tipoPremio = tipoPremio;
    }

    public int getCostoPremio() {
        return costoPremio;
    }

    public void setCostoPremio(int costoPremio) {
        this.costoPremio = costoPremio;
    }

    public String getInfoExtraPremio() {
        return infoExtraPremio;
    }

    public void setInfoExtraPremio(String infoExtraPremio) {
        this.infoExtraPremio = infoExtraPremio;
    }

    public int getEstadoPremio() {
        return estadoPremio;
    }

    public void setEstadoPremio(int estadoPremio) {
        this.estadoPremio = estadoPremio;
    }

    public int getFavorito() {
        return favorito;
    }

    public void setFavorito(int favorito) {
        this.favorito = favorito;
    }

    public String getRutaImagenHabito() {
        return rutaImagenHabito;
    }

    public void setRutaImagenHabito(String rutaImagenHabito) {
        this.rutaImagenHabito = rutaImagenHabito;
    }

}
